//Operadores comuns e iguais ao C#
//maior >
10 > 5 //true

//menor <
10 < 5 //false

//maior ou igual >=
10 >= 10 //true

//menor ou igual <=
10 <= 5 //false

//Operadores mais específicos do ECMA
//Comparação apenas dos valores ==
10 == '10' //true

//Comparação dos valores e tipo primitivo ===
10 === '10' //false